package loops;

public class ForLoopExample2 
{

	public static void main(String[] args)
{
	for(int a=1;a<=10;a=a+1)// True statement
		
	{
		
		System.out.println(a);
		
	}
			
	for(int a=1;a>=5;a=a+1)	// False Statement-" 1 is not greater than or equals to 5"
							//So Program terminates right away 
		
	{
			System.out.println(a);
	}
			

}

}
