package loops;

public class ForLoopExample3 
{

	public static void main(String[] args) 
	
{
		for(int b=1;b<=10;b=b+2) //Incrementing by +2 Every time 
		{
			System.out.println(b);
		}
		
		System.out.println("#######################################");
		
		for(int a=10;a>=1;a-- )// for decrement
							   //a-- means a=a-1
		{
			System.out.println(a);
			
		}
		
				
		
}

}
