package loops;

public class NestedForLoopExample {

	public static void main(String[] args) {
		for(int a=0;a<2;a++) // Outer Loop
		{
		for(int b=1;b<=2;b++) // Inner Loop
		{
		System.out.println(" Selenium ");
		}
		}
		System.out.println(" ##################### ");
		System.out.println();

		for(int a=0;a<2;a++) // Outer Loop
		{
		for(int b=1;b<=2;b++) // Inner Loop
		{
		System.out.println(b);
		System.out.println(a);
		}
		}


		
		
		

	}

}
